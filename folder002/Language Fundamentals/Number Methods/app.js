// Métodos de Números
var num1 = 3.1415;
console.log(typeof num1); // O type da variável num1 é number;
console.log(typeof num1.toString()); // O type agora da variável num1 é uma string;

var num2 = 50.875;
console.log(num2.toExponential(2)); // Converte o num2 para notação científica;
console.log(num2.toFixed(2)); // Estabelece duas casas decimais após a vírgula;

console.log(parseInt(num1)); // Transforma o num1 para um número inteiro;
console.log(parseFloat(num1)); // Transforma o num1 para um número decimal;
