/* ----- Statement and Expression ----- */

// Expressions are evaluated to produce a value
// They're a unit of code that resolves to a value, as instance, literals and operators

// Example

// 3;
// 1 + 2;
// "expression"
// fn()
// []
// {}

// Statement are executed to make something happen
// They're a unit of code representing one instruction or more, usually starts with a language
// reverved keyword and ends, explicitly or implicitly, with a statement terminator.

// Example

// 3;
// let x = 3;
// if() {}
// for () {}
// whie () {}
